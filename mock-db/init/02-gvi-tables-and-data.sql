ALTER SESSION SET CURRENT_SCHEMA = GVI;

CREATE TABLE GVI_FILEWHEEL_ORDER_INTERFACE (
  GVI_INTERFACE_LINE_ID NUMBER PRIMARY KEY,
  CONTEXT VARCHAR2(20 CHAR) NOT NULL,
  PROGRAM VARCHAR2(30 BYTE),
  PROCESS_FLAG VARCHAR2(2 CHAR) DEFAULT 'x' NOT NULL,
  ERROR_CODE VARCHAR2(2000 CHAR),
  FILE_NAME VARCHAR2(255 CHAR),
  LINE_NUMBER VARCHAR2(50 BYTE),
  REQUESTED_DELIVERY_DATE DATE,
  REQUESTED_QUANTITY NUMBER,
  CREATION_DATE DATE DEFAULT SYSDATE NOT NULL,
  LAST_UPDATE_DATE DATE DEFAULT SYSDATE NOT NULL,
  CURRENCY_CODE VARCHAR2(3 BYTE),
  UOM_CODE VARCHAR2(30 BYTE),
  CUSTOMER_ORDER_REFERENCE_NBR VARCHAR2(50 CHAR),
  NET_PRICE NUMBER,
  MARK VARCHAR2(240 BYTE),
  SSP_SENDER_OR_RECIPIENT VARCHAR2(20 CHAR),
  SSP_INVOICE_TYPE VARCHAR2(10 BYTE)
);

CREATE TABLE GVI_INTERNAL_ORDER_INTERFACE (
  SUBSYSTEM VARCHAR2(10 BYTE),
  CONTEXT VARCHAR2(20 BYTE) NOT NULL,
  CREATION_DATE DATE DEFAULT SYSDATE NOT NULL,
  LAST_UPDATE_DATE DATE DEFAULT SYSDATE NOT NULL,
  PROGRAM VARCHAR2(30 BYTE),
  PROCESS_FLAG VARCHAR2(2 BYTE) DEFAULT 'x',
  ERROR_CODE VARCHAR2(2000 BYTE),
  CUSTOMER_ORDER_NUMBER VARCHAR2(50 BYTE),
  ORDER_REFERENCE VARCHAR2(240 BYTE),
  LINE_NUMBER VARCHAR2(20 BYTE),
  ITEM_CODE VARCHAR2(40 BYTE),
  REQUESTED_QUANTITY NUMBER,
  REQUESTED_DATE DATE,
  SCHEDULED_QUANTITY NUMBER,
  SCHEDULE_DATE DATE,
  CURRENCY_CODE VARCHAR2(3 BYTE),
  NET_PRICE NUMBER,
  GVI_FW_INTERFACE_LINE_ID NUMBER,
  DIRECTION VARCHAR2(50 BYTE),
  CUSTOMER_ORDER_REFERENCE_NBR VARCHAR2(50 CHAR)
);

CREATE INDEX IX_GVI_FWOI_TRACK
  ON GVI_FILEWHEEL_ORDER_INTERFACE (CUSTOMER_ORDER_REFERENCE_NBR, FILE_NAME, PROGRAM, PROCESS_FLAG);

CREATE INDEX IX_GVI_IOI_TRACK
  ON GVI_INTERNAL_ORDER_INTERFACE (CUSTOMER_ORDER_REFERENCE_NBR, DIRECTION, PROCESS_FLAG);

